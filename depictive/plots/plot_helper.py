
from matplotlib.cm import ScalarMappable as scm

# =======================================================
# =======================================================
# coluer array
# =======================================================
# =======================================================

def get_coluer(N, cmap='viridis'):
    coluer_map = scm(cmap=cmap)
    if type(N) != int:
        N = int(N)
    return coluer_map.to_rgba(range(N))


# =======================================================
# =======================================================
# save figure
# =======================================================
# =======================================================

def save(fig, sname):
    if sname:
        fig.savefig(sname, fmt=sname.split('.')[-1])
